package com.piaoyou.bean.movie;

public class DateControl {
	private String dateControl;
	private boolean hasShowtime;
	private String cityName;
	private String imageUrl;
	private int totalCinemaCount;
	private int totalShowtimeCount;
	private int cinemaFilterControl;
	private String cinemaIds;
	private String movieShowtimeListControl;
	public String getDateControl() {
		return dateControl;
	}
	public void setDateControl(String dateControl) {
		this.dateControl = dateControl;
	}
	public boolean isHasShowtime() {
		return hasShowtime;
	}
	public void setHasShowtime(boolean hasShowtime) {
		this.hasShowtime = hasShowtime;
	}
	public String getCityName() {
		return cityName;
	}
	public void setCityName(String cityName) {
		this.cityName = cityName;
	}
	public String getImageUrl() {
		return imageUrl;
	}
	public void setImageUrl(String imageUrl) {
		this.imageUrl = imageUrl;
	}
	public int getTotalCinemaCount() {
		return totalCinemaCount;
	}
	public void setTotalCinemaCount(int totalCinemaCount) {
		this.totalCinemaCount = totalCinemaCount;
	}
	public int getTotalShowtimeCount() {
		return totalShowtimeCount;
	}
	public void setTotalShowtimeCount(int totalShowtimeCount) {
		this.totalShowtimeCount = totalShowtimeCount;
	}
	public int getCinemaFilterControl() {
		return cinemaFilterControl;
	}
	public void setCinemaFilterControl(int cinemaFilterControl) {
		this.cinemaFilterControl = cinemaFilterControl;
	}
	public String getCinemaIds() {
		return cinemaIds;
	}
	public void setCinemaIds(String cinemaIds) {
		this.cinemaIds = cinemaIds;
	}
	public String getMovieShowtimeListControl() {
		return movieShowtimeListControl;
	}
	public void setMovieShowtimeListControl(String movieShowtimeListControl) {
		this.movieShowtimeListControl = movieShowtimeListControl;
	}
	

}

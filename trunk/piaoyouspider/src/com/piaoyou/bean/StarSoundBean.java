package com.piaoyou.bean;

public class StarSoundBean {
	private int starSoundId;
	private int starId;
	private String soundTitle;
	private String soundUrl;
	private String carwledTime;

	public int getStarSoundId() {
		return starSoundId;
	}
	public void setStarSoundId(int starSoundId) {
		this.starSoundId = starSoundId;
	}
	public int getStarId() {
		return starId;
	}
	public void setStarId(int starId) {
		this.starId = starId;
	}
	public String getSoundTitle() {
		return soundTitle;
	}
	public void setSoundTitle(String soundTitle) {
		this.soundTitle = soundTitle;
	}
	public String getSoundUrl() {
		return soundUrl;
	}
	public void setSoundUrl(String soundUrl) {
		this.soundUrl = soundUrl;
	}
	public String getCarwledTime() {
		return carwledTime;
	}
	public void setCarwledTime(String carwledTime) {
		this.carwledTime = carwledTime;
	}
}

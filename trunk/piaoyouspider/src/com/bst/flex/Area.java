package com.bst.flex;

public class Area {

	private String areaBlockColor;
	private String profileImgPath;
	private String regionname;
	private String memo;
	private String areaName;
	private boolean sellable;
	private String areaType;
	private boolean hasTickets;
	private String areaTip;
	private String areaId;
	private String xy;
	
	
	public String getAreaBlockColor() {
		return areaBlockColor;
	}
	public void setAreaBlockColor(String areaBlockColor) {
		this.areaBlockColor = areaBlockColor;
	}
	public String getProfileImgPath() {
		return profileImgPath;
	}
	public void setProfileImgPath(String profileImgPath) {
		this.profileImgPath = profileImgPath;
	}
	public String getRegionname() {
		return regionname;
	}
	public void setRegionname(String regionname) {
		this.regionname = regionname;
	}
	public String getMemo() {
		return memo;
	}
	public void setMemo(String memo) {
		this.memo = memo;
	}
	public String getAreaName() {
		return areaName;
	}
	public void setAreaName(String areaName) {
		this.areaName = areaName;
	}
	public boolean isSellable() {
		return sellable;
	}
	public void setSellable(boolean sellable) {
		this.sellable = sellable;
	}
	public String getAreaType() {
		return areaType;
	}
	public void setAreaType(String areaType) {
		this.areaType = areaType;
	}
	public boolean isHasTickets() {
		return hasTickets;
	}
	public void setHasTickets(boolean hasTickets) {
		this.hasTickets = hasTickets;
	}
	public String getAreaTip() {
		return areaTip;
	}
	public void setAreaTip(String areaTip) {
		this.areaTip = areaTip;
	}
	public String getAreaId() {
		return areaId;
	}
	public void setAreaId(String areaId) {
		this.areaId = areaId;
	}
	public String getXy() {
		return xy;
	}
	public void setXy(String xy) {
		this.xy = xy;
	}
}
